/**
 * Download file with branded filename
 */
export function downloadBrandedFile(
  store: StoreData,
  content: string,
  type: 'csv' | 'html',
  reportName: string
) {
  const timestamp = new Date().toISOString().slice(0, 10);
  const storeName = store.name.toLowerCase().replace(/[^a-z0-9]/g, '-');
  const filename = `${storeName}-${reportName}-${timestamp}.${type}`;
  
  const mimeType = type === 'csv' ? 'text/csv' : 'text/html';
  const blob = new Blob([content], { type: mimeType });
  const url = window.URL.createObjectURL(blob);
  
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  window.URL.revokeObjectURL(url);
}

/**
 * Get store brand styles for components
 */
export function getStoreBrandStyles(store: StoreData | null) {
  const primaryColor = store?.primaryColor || '#3B82F6';
  
  return {
    primaryColor,
    buttonStyle: {
      backgroundColor: primaryColor,
      borderColor: primaryColor,
    },
    linkStyle: {
      color: primaryColor,
    },
    headerStyle: {
      borderBottomColor: primaryColor,
    },
    badgeStyle: {
      backgroundColor: `${primaryColor}20`,
      color: primaryColor,
    }
  };
}