// Store branding utilities for documents
export interface StoreData {
  id: string;
  name: string;
  description?: string;
  address: string;
  phone: string;
  email: string;
  logoUrl?: string;
  primaryColor: string;
}

/**
 * Generate CSV with store branding header
 */
export function generateBrandedCSV(
  store: StoreData,
  data: Record<string, unknown>[],
  headers: string[],
  title: string
): string {
  const lines: string[] = [];
  
  // Add store header
  lines.push(`${store.name}`);
  lines.push(`${store.address}`);
  lines.push(`Phone: ${store.phone} | Email: ${store.email}`);
  lines.push(''); // Empty line
  lines.push(title);
  lines.push(`Generated on: ${new Date().toLocaleString()}`);
  lines.push(''); // Empty line
  
  // Add table headers
  lines.push(headers.join(','));
  
  // Add data rows
  data.forEach(row => {
    const values = headers.map(header => {
      const value = row[header] || '';
      // Escape commas and quotes in CSV
      return typeof value === 'string' && (value.includes(',') || value.includes('"'))
        ? `"${value.replace(/"/g, '""')}"`
        : value;
    });
    lines.push(values.join(','));
  });
  
  return lines.join('\n');
}

